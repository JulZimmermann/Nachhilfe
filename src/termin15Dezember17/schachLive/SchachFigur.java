package termin15Dezember17.schachLive;

public class SchachFigur {

    private String farbe;
    private String typ;

    public SchachFigur(String farbe, String typ) {
        this.farbe = farbe;
        this.typ = typ;
    }

    public String getFarbe() {
        return farbe;
    }

    public String getTyp() {
        return typ;
    }

}
